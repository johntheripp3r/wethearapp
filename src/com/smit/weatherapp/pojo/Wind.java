package com.smit.weatherapp.pojo;

/**
 * @author johntheripp3r
 */
public class Wind
{
	private float speed;
	private float deg;
	
	public Wind()
	{
	}
	
	public float getSpeed()
	{
		return this.speed;
	}
	
	public void setSpeed(float speed)
	{
		this.speed = speed;
	}
	
	public float getDeg()
	{
		return this.deg;
	}
	
	public void setDeg(float deg)
	{
		this.deg = deg;
	}
	
}
